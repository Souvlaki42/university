CREATE TABLE EMP (
  EMPNO INT(4) NOT NULL,
  ENAME VARCHAR(10),
  JOB VARCHAR(25),
  HIREDATE DATE,
  MGR INT(4),
  SAL FLOAT(7,2),
  COMM FLOAT(7,2),
  DEPTNO INT(2),
  PRIMARY KEY(EMPNO),
  FOREIGN KEY(DEPTNO) REFERENCES DEPT(DEPTNO)
);

